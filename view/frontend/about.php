                <h5 class="description"><p>Jean Forteroche est né le 26 octobre 1958 en France.
        À 19 ans, il s'engage à la Croix Rouge Française où il passe 5 ans à divers postes. 
        Parallèlement, il étudie la gestion et l'économie à l'Université Paris Dauphine.
        En 1983 il crée une société spécialisée dans l'agro-alimentaire en France et aux États-Unis. 
        En 1991 il perd le contrôle de son groupe et démissionne.
        À 31 ans, repartant de zéro, il rentre à Paris et fonde avec trois amis une société de gestion de patrimoine qui deviendra l'un 
        des premiers cabinets de ce secteur en France.</p>
        <p>À 37 ans, Jean Forteroche écrit une histoire à son fils Marcel. Encouragé par sa 
            tante scénariste (aujourd'hui réalisatrice), il envoie ce manuscrit aux Éditions 
            Ricochet qui acceptent aussitôt de publier <em>Et Pourquoi pas..</em>. Peu avant la 
            sortie du roman, Agatha Marla (BestMovies Inc.) en acquiert les droits d'adaptation 
            cinématographique. Le film, intitulé Just Like That, produit par Agatha Marla, 
            interprété par Mini Douvinetu et Fart Dumuno, s'est classé premier du box-office 
            américain lors de sa sortie en 2006.
        Après la publication de son premier roman, <em>Et pourquoi pas..</em>, en 2000, Jean 
        Forteroche se consacre exclusivement à l'écriture. Tous ses romans figurent dès leur 
        parution en tête des ventes annuelles en France et connaissent un succès international. </p>
        <p>Jean Forteroche a écrit quelques nouvelles et a réalisé un court métrage pour La 
            Croix Rouge, <em>La missive du Boiteux</em>. 
        Son roman <em>L'appel du triton chahuteur</em>a été adapté en bande dessinée par Vivel. 
        La première partie est sortie le 31 aout 2010, la seconde partie le 16 mars 2011.
        Son roman <em>Les vacanciers des îles perdues</em> a été adapté en série d'animation 
        par Tif Parl. La série est sortie le 12 septembre 2013.</p>
        <p>Sa nouvelle oeuvre, <em>Un billet simple pour l'Alaska</em> est actuellement en cours 
        d'écriture. Il est cependant partiellement disponible sur son <span title="unbilletsimplepourlalaska.com">site</span>, en attendant la parution du récit complet.</p></h5>
